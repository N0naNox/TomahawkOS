/**
 * @brief Draw a character from the font to framebuffer
 * @param fb Framebuffer pointer
 * @param pitch Framebuffer pitch in pixels per line
 * @param c Character to draw
 * @param x X coordinate
 * @param y Y coordinate
 * @param width Framebuffer width
 * @param height Framebuffer height
 */
static inline void draw_char_8x16(volatile uint32_t* fb, uint32_t pitch, char c, 
                                   uint32_t x, uint32_t y, uint32_t width, uint32_t height)
{
	if (c < 32 || c > 126) return;
	
	const uint8_t* bitmap = font_8x16[c - 32];
	
	/* Draw each row of the character (16 rows) */
	for (int row = 0; row < 16; row++) {
		uint8_t bits = bitmap[row];
		/* Draw each pixel in the row (8 pixels wide) - bit 0 is leftmost */
		for (int col = 0; col < 8; col++) {
			if (bits & (1 << col)) {
				uint32_t px = x + col;
				uint32_t py = y + row;
				if (px < width && py < height) {
					fb[py * pitch + px] = 0xFFFFFFFF;  /* White */
				}
			}
		}
	}
}

